 function pricesort(){
   var selected=document.querySelector("#newprice").value;

   if(selected=="lth"){
     saleData.sort(function(a,b){
       return a.price-b.price;
     });
     displayData(saleData);
   }

   if(selected=="htl"){
    saleData.sort(function(a,b){
      return b.price-a.price;
    });
    displayData(saleData);
  }

 }
 
 function namesort(){
   var selected= document.querySelector("#Alphabets").value;

   if(selected=="asc"){
     saleData.sort(function(a,b){
       if(a.name>b.name) return 1;
       if(a.name<b.name) return -1;
       return 0;
     });
     displayData(saleData);
   }

   if(selected=="dsc"){
    saleData.sort(function(a,b){
      if(a.name>b.name) return -1;
      if(a.name<b.name) return 1;
      return 0;
    });
    displayData(saleData);
  }
 }


